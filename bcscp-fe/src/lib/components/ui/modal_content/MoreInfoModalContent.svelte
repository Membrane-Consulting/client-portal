<h2>What is this?</h2>

<p>This dashboard is a tool designed & built by Black Cat Studio, for our clients to be able to manage their business with us in a simpler way. There are four pages in this dashboard...</p>
<ul>
  <li>Sites - Contains a list of all your websites built by us including some analytics/ data.</li>
  <li>Invoices - A table of all invoices issued by BCS including an integration that allows you to pay invoices & download a pdf version.</li>
  <li>Assets - This tab contains any and all branding work completed by BCS, allowing downloads where appropriate.</li>
  <li>Contracts - View or download a PDF of any work agreement between BCS & the client.</li>
</ul>